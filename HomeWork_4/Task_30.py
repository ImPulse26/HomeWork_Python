# Вычислить число π c заданной точностью d
# Пример:
# при
# d = 0.001, π = 3.141             10−1≤d≤10−10
# d = 0.001, 𝜋 = 3.141             10−1≤d≤10−10

d = input('Введите d => ')
k = 1
pi = 0

for i in range(1000000):
    if i % 2 == 0:
        pi += 4 / k
    else:
        pi -= 4 / k
    k += 2

print(f'Число {pi} с точностью {d} => {str(pi)[:len(d)]}')
